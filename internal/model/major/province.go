// isNeighborProvince 判断两个省份是否相邻
func isNeighborProvince(province1, province2 string) bool {
	neighbors, exists := neighborProvinces[province1]
	if !exists {
		return false
	}
	for _, neighbor := range neighbors {
		if neighbor == province2 {
			return true
		}
	}
	return false
}

// isSameRegion 判断两个省份是否在同一区域
func isSameRegion(province1, province2 string) bool {
	region1, exists1 := provinceRegions[province1]
	region2, exists2 := provinceRegions[province2]
	if !exists1 || !exists2 {
		return false
	}
	return region1 == region2
} 